input('Hola, en este ejercicio calcularemos el area y perimetro de un rectangulo y tu me daras los valores. '
     '(Da enter para continuar) ')
alto = int(input('Proporciona el alto: '))
ancho = int(input('Proporciona el ancho: '))
area = alto * ancho
perimetro = (alto * ancho) * 2
print(f'Area: {area}')
print(f'Perimetro: {perimetro}')


