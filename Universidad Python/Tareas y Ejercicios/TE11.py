#De int a Str
numero = int(input('Dame un numero del 5 al 10: '))

if numero == 10:
    print(f'El numero {numero} se escribe Diez')
elif numero == 9:
    print(f'El numero {numero} se escribe Nueve')
elif numero == 8:
    print(f'El numero {numero} se escribe Ocho')
elif numero == 7:
    print(f'El numero {numero} se escribe Siete')
elif numero == 6:
    print(f'El numero {numero} se escribe Seis')
elif numero == 5:
    print(f'El numero {numero} se escribe Cinco ')
else:
    print('El numero no entra dentro del rango pedido.')
