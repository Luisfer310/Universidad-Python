# Hare una operacion para cada operador visto en el tema de 'Operadores aritméticos en Python', en la sección 5.

# Suma
a = 5
b = 4
resultado = a + b
print(f'El resultado de la suma es: {resultado}')

# Resta
resultado = a - b
print(f'El resultado de la resta es: {resultado}')

# Division
resultado = a / b
print(f'El resultado de la division es: {resultado}')

# Multiplicación
resultado = a * b
print(f'El resultado de la multiplicacion es: {resultado}')

# Exponencial
resultado = a**b
print(f'El resultado de elevar {a} a la {b} potencia es: {resultado}')

# Modulo
resultado = a % b
print(f'El resultado de modulo de {a} entre {b} es: {resultado}')

# Division de enteros
resultado = a//b
print(f'El resultado de la division de enteros es: {resultado}')
