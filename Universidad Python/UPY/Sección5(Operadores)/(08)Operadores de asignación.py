# Asignación
miVariable = 10

# reasignacion común
miVariable = miVariable + 1
print(miVariable)

# Reasignación corta
miVariable += 1
print(miVariable)

# Reasignación con otros operadores
miVariable *= 2
print(miVariable)

miVariable /= 2
print(miVariable)

miVariable %= 2
print(miVariable)

miVariable **= 2
print(miVariable)

miVariable //= 2
print(miVariable)

miVariable -= 2
print(miVariable)
