# Los operadores son los siguentes...
# == (igual a)
# ≠ ó != (diferente a)
# > (mayor que)
# < (menor que)
# ≥ ó >= (mayor o igual)
# ≤ ó >= (menor o igual)

# Igual a
a = 5
b = 8
resultado = a == b
print(resultado)

# Diferente a
a = 5
b = 8
resultado = a != b
print(resultado)

# Mayor a
a = 5
b = 8
resultado = a > b
print(resultado)

# Menor a
a = 5
b = 8
resultado = a < b
print(resultado)

# Mayor o igual
a = 5
b = 8
resultado = a >= b
print(resultado)

# Menor o igual
a = 5
b = 8
resultado = a <= b
print(resultado)
