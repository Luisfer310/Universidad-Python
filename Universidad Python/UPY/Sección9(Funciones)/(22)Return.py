# Return en funcion, remplazando a print
def suma(num1, num2, num3):
    resultado = num1 + num2 + num3
    return resultado


# Guardar el return de la funcion en una variable
resultadoDeSuma = suma(3, 6, 9)
print(resultadoDeSuma)

# Pintar el return de la funcion sin guardarla en una variable
print(suma(3, 6, 9))
