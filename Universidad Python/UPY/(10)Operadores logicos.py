# Los operadores logicos son:
# and, or y not
# En código se puede usar de la siguiente manera...

# and
a = 5
b = 7
if a <= 5 and b >= 7:
    print("La condición es correcta")
else:
    print("No es correcta")

# or
a = 7
b = 9
if a < 3 or b == 9:
    print("La condición es correcta")
else:
    print("No es correcta")

# not
x = False
if not x:
    print('Las condiciones coinciden')
else:
    print('Las condiciones no coinciden')

