# Manejo de cadenas en Python

# Concatenar cadenas con signo de concatenador
nombre = 'Luis'
apellido = 'Osuna'
print('Mi nombre es ' + nombre + ' y me apellido ' + apellido)

# Concatenar con el signo coma
nombre = 'Luis'
apellido = 'Osuna'
print('Mi nombre es', nombre, 'y me apellido', apellido)

# Convertir string en integers para en lugar de concatenar, sumemos los elementos
a = '5'
b = '7'
print(int(a) + int(b))
